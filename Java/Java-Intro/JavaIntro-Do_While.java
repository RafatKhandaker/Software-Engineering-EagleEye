public class example_Do_While_Loop {

	public static void main(String[] args) {
		
// In this example x is set to 10. the program will print out x value
// decrement the value
// while the value of x is greater than 0.
		
		int x = 10;
		
		do{
			System.out.println("x = " + x);
			x--;
		}while(x > 0);

	}

}
